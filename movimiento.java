public class movimiento {
    public int xDestino;
    public int yDestino;
    public int xOrigen;
    public int yOrigen;
    movimiento(int xDestino, int yDestino, int xOrigen, int yOrigen){
        this.xDestino = xDestino;
        this.yDestino = yDestino;
        this.xOrigen = xOrigen;
        this.yOrigen = yOrigen;
    }

}
